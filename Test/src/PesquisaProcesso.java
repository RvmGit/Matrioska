package matrioska;

public class PesquisaProcesso {
	private int nivelSatisfacao;
	private String texto;
	
	private int numeroDeChar;
	private int identificador;
	public int getNivelSatisfacao() {
		return nivelSatisfacao;
	}
	public void setNivelSatisfacao(int nivelSatisfacao) {
		this.nivelSatisfacao = nivelSatisfacao;
	}
	public String getTexto() {
		return texto;
	}
	public void setTexto(String texto) {
		this.texto = texto;
	}
	public int getNumeroDeChar() {
		return numeroDeChar;
	}
	public void setNumeroDeChar(int numeroDeChar) {
		this.numeroDeChar = numeroDeChar;
	}
	public int getIdentificador() {
		return identificador;
	}
	public void setIdentificador(int identificador) {
		this.identificador = identificador;
	}
	
	
}
